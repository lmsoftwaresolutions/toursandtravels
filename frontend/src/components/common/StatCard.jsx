export default function StatCard({ 
  icon, 
  title, 
  value, 
  subtitle, 
  gradient = "from-blue-500 to-blue-700",
  onClick 
}) {
  const baseClasses = `bg-gradient-to-br ${gradient} text-white p-6 rounded-xl shadow-lg transition-all`;
  const interactiveClasses = onClick 
    ? "hover:shadow-xl transform hover:-translate-y-1 cursor-pointer" 
    : "";

  return (
    <div 
      className={`${baseClasses} ${interactiveClasses}`}
      onClick={onClick}
      role={onClick ? "button" : undefined}
      tabIndex={onClick ? 0 : undefined}
    >
      {icon && (
        <div className="bg-white bg-opacity-20 p-2 rounded-lg w-fit mb-2">
          {icon}
        </div>
      )}
      <p className="text-sm opacity-90 mb-1">{title}</p>
      <p className="text-3xl font-bold">{value}</p>
      {subtitle && <p className="text-xs opacity-75 mt-2">{subtitle}</p>}
    </div>
  );
}

